import { axios } from '../../axios'

//--------------------------------------------------------------------------------------------------------------------------------------------
export const CreatePost = (post) => {
  axios.post(`post`, post).then((post) => post.json())
}

//--------------------------------------------------------------------------------------------------------------------------------------------
export const GetPost = (postId) => axios.get(`post/getPost/${postId}`)

//--------------------------------------------------------------------------------------------------------------------------------------------
export const GetPosts = () => axios.get(`post/getPosts`)

//--------------------------------------------------------------------------------------------------------------------------------------------
export const UpdatePost = (postId, post) =>
  axios.put(`post/putPost/${postId}`, post)

//--------------------------------------------------------------------------------------------------------------------------------------------
export const DeletePost = (postId) => axios.delete(`post/deletePost/${postId}`)
